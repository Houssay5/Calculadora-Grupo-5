def multiplicacion():
    numero1 = float(input("Ingrese el primer número a multiplicar: "))
    numero2 = float(input("Ingrese el segundo número a multiplicar: "))
    resultadoMultiplicacion = int(numero1 * numero2)
    print("El resultado de la multiplicación es:", resultadoMultiplicacion)

multiplicacion()
